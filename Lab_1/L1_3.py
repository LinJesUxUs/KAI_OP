import math

X = int(input("Введите массу шоколада кг "))
print("Масса шоколада = ", X)
A = int(input("Введите цену шоколада "))
print("Цена шоколада массой ", X, "равна = ", A)

Y = int(input("Введите массу ирисок кг "))
print("Масса ирисок = ", Y)
B = int(input("Введите цену ирисок "))
print("Цена ирисок массой ", Y, "равна = ", B)

ax = A / X
by = B / Y

print("1 кг шоколадных конфет стоит = ", ax)
print("1 кг ирисок стоит = ", by)
print("Шоколадные конфеты дороже ирисок в ", ax/by , "раз")
